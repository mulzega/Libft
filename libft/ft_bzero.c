#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = s;
	while (n > 0)
	{
		*str = '\0';
		str++;
		n--;
	}
	return (s);
}
